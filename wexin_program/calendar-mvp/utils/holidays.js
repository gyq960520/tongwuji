/**
 * 是否为法定节假日（含调休出来的）
 * @param {string} dateStr 'YYYY-MM-DD'
 */
function isHoliday(dateStr) {
  return !!HOLIDAYS[dateStr]
}

/**
 * 是否为调休工作日（周末但要上班）
 * @param {string} dateStr 'YYYY-MM-DD'
 */
function isWorkday(dateStr) {
  return !!WORKDAYS[dateStr]
}

/**
 * 获取节假日名称（用于未来扩展显示）
 */
function getHolidayName(dateStr) {
  return HOLIDAYS[dateStr] || ''
}

